package com.step.calculator;

public class Calculator {

    public double adunare(double nr1, double nr2) {
        return nr1 + nr2;
    }

    public double scadere(double nr1, double nr2) {
        return nr1 - nr2;
    }

    public double inmultire(double nr1, double nr2) {
        return nr1 * nr2;
    }

    public double impartire(double nr1, double nr2) {
        return nr1 / nr2;
    }

}
