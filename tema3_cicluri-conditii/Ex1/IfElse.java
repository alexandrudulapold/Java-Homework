public class IfElse {
	
	public static void main(String[] args) {
		
		char litera1 = 'i';
		
		if(litera1 == 'a' || litera1 == 'i' || litera1 == 'e' || litera1 == 'o' || litera1 == 'u') {
			System.out.println("Litera " + litera1 + " este o vocala");
		}
		else {
			System.out.println("Litera " + litera1 + " este o consoana");
		}
		
		
	}
	
}